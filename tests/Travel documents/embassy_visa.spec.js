    await selectors.dropdownOptions(page, "dropdown-applicant.0.income_triage", "Over 2500 USD monthly")
    await selectors.booleanOptions(page, "applicant.0.own_state_triage", "option-Yes")

    const other_countries_travel = page.getByTestId("option-Yes, in the last 5 years, I have traveled or have a visa for at least 1 of these countries - USA, Canada, Australia, UK, all Schengen countries.")
    await other_countries_travel.click()
    const visa_denied = page.getByTestId("option-I was denied this visa over 12 months ago")
    await visa_denied.click()
    await page.locator('[name="applicant.0.birth_city"]').fill("test")
    await selectors.addressApi(page, "applicant.0.home_address")
    await page.waitForTimeout(1000)
    await selectors.dropdownSelector(page, "applicant.0.secondary_document_issued_country", "dropdown-applicant.0.secondary_document_issued_country", "Mexico", "Mexico flag Mexico")
