    await selectors.dropdownOptions(page, "dropdown-applicant.0.marital_status", "Single / Never Married")
    await selectors.booleanOptions(page, "applicant.0.family_residents", "option-No")
    await selectors.booleanOptions(page, "applicant.0.family_traveling_with", "option-Yes")
    await expect(next_btn).toBeEnabled()
    await next_btn.click()
    await page.waitForURL(deploy_url + "order/" + Order_num + "/continue/#step=trav0_travel")
