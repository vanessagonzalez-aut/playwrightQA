    await expect(next_btn).toBeEnabled()
    await next_btn.click()
    await page.waitForURL(deploy_url + "order/" + Order_num + "/continue/#step=trav0_past_travel")
    await selectors.booleanOptions(page ,"applicant.0.previously_visited", "option-No")
    await selectors.booleanOptions(page ,"applicant.0.current_visas", "option-No")
    await selectors.booleanOptions(page ,"applicant.0.refused_visa", "option-No")
